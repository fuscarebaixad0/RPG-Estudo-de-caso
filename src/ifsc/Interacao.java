package ifsc;

public class Interacao {

	private String habitante;
	private String companheiro;
	private String criatura;

	public String getHabitante() {
		return habitante;
	}

	public void setHabitante(String habitante) {
		this.habitante = habitante;
	}

	public String getCompanheiro() {
		return companheiro;
	}

	public void setCompanheiro(String companheiro) {
		this.companheiro = companheiro;
	}

	public String getCriatura() {
		return criatura;
	}

	public void setCriatura(String criatura) {
		this.criatura = criatura;
	}

}
